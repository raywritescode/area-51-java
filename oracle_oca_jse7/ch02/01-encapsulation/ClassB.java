class ClassB {
   private int size;
   private String name;
   private String color;

   public void setSize( int s ) {
      size = s;
   }

   public int getSize() {
      return size;
   }

   public void setName( String n ) {
      name = n;
   }

   public String getName() {
      return name;
   }

   public void setColor( String c ) {
      color = c;
   }

   public String getColor() {
      return color;
   }
}
